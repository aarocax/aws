    public static function getTokenFromFile($path, $ttl = 864000) {
        $now = time();
        try {
            $content = self::getContentsWithLock($path);
            $dataToken = json_decode($content);
            if (isset($dataToken->ttl) && is_int($dataToken->ttl)) {
                $ttl = $dataToken->ttl;
            }
            if (is_int($ttl)) {
                if (isset($dataToken->time) && is_int($dataToken->time) && $now - $dataToken->time < $ttl) {
                    return $dataToken->token;
                }
            } else {
                return $dataToken->token;
            }
        } catch (\Exception $e) {}
        return false;
    }

    public static function putToken2File($path, $token, $ttl = 864000, $overwrite = false, $waitIfLocked = true) {
        $now = time();

        $fileExists = file_exists($path);
        if ($fileExists) {
            $fp = fopen($path, "r+");
        } else {
            $fp = fopen($path, "w");
        }
        if ($fp) {
            $locked = flock($fp, LOCK_EX, $waitIfLocked);
            
            if (!$locked) {
                return false;
            }
            else {
                if ($fileExists && !$overwrite) {
                    // $content = file_get_contents($path);
                    while (!feof($fp)) {
                        $contents[] = fgets($fp, 9999);
                    }
                    if (sizeof($contents) > 0) {
                        $content = join("\n", $contents);
                        $dataToken = json_decode($content);
                        if (isset($dataToken->ttl) && isset($dataToken->time) && is_int($dataToken->time) && is_int($dataToken->ttl) && $now - $dataToken->time < $dataToken->ttl && isset($dataToken->token)) {
                            flock($fp, LOCK_UN);
                            fclose($fp);
                            return $dataToken->token;
                        }
                    }
                }
                ftruncate($fp, 0);
                fprintf($fp, "{\"token\": \"%s\", \"time\": %d, \"ttl\": %d}\n", $token, $now, $ttl);
                fflush($fp);
                flock($fp, LOCK_UN);
                fclose($fp);
            }
            return true;
        }
        return false;
    }

    public static function getContentsWithLock($path, $waitIfLocked = true)
    {
        if (!file_exists($path)) {
            throw new \Exception(sprintf("File \"%s\" does not exists", $path));
        }
        else {
            $fp = fopen($path, 'r');
            $locked = flock($fp, LOCK_SH, $waitIfLocked);
           
            if (!$locked) {
                return false;
            }
            else {
                // $content = file_get_contents($path);

                while (!feof($fp)) {
                    $contents[] = fgets($fp, 9999);
                }
                $content = join("\n", $contents);
                flock($fp, LOCK_UN);
                fclose($fp);
               
                return trim($content);
            }
        }
    }

    public static function putContentsWithLock($path, $content, $waitIfLocked = true): bool
    {
        $fp = fopen($path, "r+");
        $locked = flock($fp, LOCK_EX, $waitIfLocked);
           
        if (!$locked) {
            return false;
        }
        else {
            ftruncate($fp, 0);
            fprintf($fp, "%s\n", $content);
            fflush($fp);
            flock($fp, LOCK_UN);
            fclose($fp);
        }
        return true;
    }

    
